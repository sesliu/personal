package com.personal.modelo;

public class Profissao {

	private Integer idProfissao;
	private Integer dia;
	private String nome;
	private Integer mes;
	private String mesNome;
	
	
	
	
	
	public String getMesNome() {
		return mesNome;
	}
	public void setMesNome(String mesNome) {
		this.mesNome = mesNome;
	}
	public Integer getMes() {
		return mes;
	}
	public void setMes(Integer mes) {
		this.mes = mes;
	}
	public Integer getIdProfissao() {
		return idProfissao;
	}
	public void setIdProfissao(Integer idProfissao) {
		this.idProfissao = idProfissao;
	}
	public Integer getDia() {
		return dia;
	}
	public void setDia(Integer dia) {
		this.dia = dia;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	
	
}
